// 获取真题库列表（用于前端展示）
export function getQuestionBankList() {
  const list = [];
  for (const [company, positions] of Object.entries(questionBank)) {
    for (const [position, stages] of Object.entries(positions)) {
      list.push({
        company,
        position,
        stages: Object.keys(stages).map((key) => ({
          key,
          ...stages[key],
        })),
      });
    }
  }
  return list;
}

// 获取指定真题
export function getQuestionBank(company, position, stage) {
  return questionBank[company]?.[position]?.[stage] || null;
}
